function bold() {
  let g =document.getElementById("mimi");
  if (g.style.fontWeight=="normal")
  g.style.fontWeight = "bold"
else g.style.fontWeight="normal"
}
function underlined() {
let s=document.getElementById("mimi");
if (s.style.fontWeight=="normal")
  s.style.fontWeight = "underlined"
else s.style.fontWeight="normal"
}
